print("aplikasi menghitung luas dan keliling balok")

# atur nilai variabel
panjang = 10
lebar = 5
tinggi = 5

# rumus 
luas = ((2 * panjang * lebar) + (2 * panjang * tinggi) + (2 * lebar * tinggi))
